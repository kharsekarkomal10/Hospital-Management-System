    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();

        txtPID.setText(jTable1.getValueAt(row, 0).toString());
        txtPname.setText(jTable1.getValueAt(row, 1).toString());
        txtPhoneNo.setText(jTable1.getValueAt(row, 2).toString());
        txtPhoneNo1.setText(jTable1.getValueAt(row, 3).toString());
        txtPhoneNo2.setText(jTable1.getValueAt(row, 4).toString());
        jComboBox1.setSelectedItem(jTable1.getValueAt(row, 5).toString());
        jSpinner1.setValue(Integer.parseInt(jTable1.getValueAt(row, 6).toString()));
        txtAddress.setText(jTable1.getValueAt(row, 7).toString());
    }//GEN-LAST:event_jTable1MouseClicked

