    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
       try {
        pst = con.prepareStatement(
            "INSERT INTO lower_staff VALUES (?,?,?,?,?,?,?,?)"
        );

        pst.setString(1, txtPID.getText());
        pst.setString(2, txtPname.getText());
        pst.setString(3, txtPhoneNo.getText());
        pst.setString(4, txtPhoneNo1.getText()); // staff type
        pst.setString(5, txtPhoneNo2.getText()); // city
        pst.setString(6, jComboBox1.getSelectedItem().toString()); // gender
        pst.setInt(7, (int) jSpinner1.getValue()); // age
        pst.setString(8, txtAddress.getText());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Staff Added Successfully");

        tableLoad();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
      
    try {
        pst = con.prepareStatement(
            "UPDATE lower_staff SET staff_name=?, phone_no=?, staff_type=?, city=?, gender=?, age=?, address=? WHERE staff_id=?"
        );

        pst.setString(1, txtPname.getText());
        pst.setString(2, txtPhoneNo.getText());
        pst.setString(3, txtPhoneNo1.getText());
        pst.setString(4, txtPhoneNo2.getText());
        pst.setString(5, jComboBox1.getSelectedItem().toString());
        pst.setInt(6, (int) jSpinner1.getValue());
        pst.setString(7, txtAddress.getText());
        pst.setString(8, txtPID.getText());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Updated Successfully");

        tableLoad();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Update Failed");
    }

    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        try {

        String pid = txtPID.getText().trim();

        pst = con.prepareStatement("DELETE FROM lower_staff WHERE staff_id=?");    
        pst.setString(1, pid);

        int result = pst.executeUpdate();

        if(result > 0) {
            JOptionPane.showMessageDialog(this, "Deleted Successfully");
            tableLoad();
        } else {
            JOptionPane.showMessageDialog(this, 
                "No matching Patient ID in database.\nCheck exact value in table.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btndeleteActionPerformed

